// ============================================================================
// BmhSearcher
// ============================================================================

void BmhSearcher::build(const char* query_lower, i32 qlen)
{
	if (qlen >= (i32)sizeof(needle)) qlen = (i32)sizeof(needle) - 1;
	memcpy(needle, query_lower, qlen);
	needle[qlen] = '\0';
	needle_len = qlen;

	for (i32 i = 0; i < 256; i++) skip[i] = qlen;
	for (i32 i = 0; i < qlen - 1; i++) skip[(u8)needle[i]] = qlen - 1 - i;
}

const char* BmhSearcher::search(const char* hay, i32 hlen) const
{
	if (needle_len == 0) return hay;
	if (needle_len > hlen) return nullptr;
	i32 last = needle_len - 1;
	i32 limit = hlen - needle_len;
	i32 pos = 0;
	while (pos <= limit) {
		i32 j = last;
		while (j >= 0 && g_lower[(u8)hay[pos + j]] == (u8)needle[j]) j--;
		if (j < 0) return hay + pos;
		pos += skip[g_lower[(u8)hay[pos + last]]];
	}
	return nullptr;
}

// ============================================================================
// Line helpers
// ============================================================================

i32 count_newlines(const char* start, const char* end)
{
	i32 c = 0;
	for (const char* p = start; p < end; p++) if (*p == '\n') c++;
	return c;
}

const char* find_line_start(const char* pos, const char* buf_start)
{
	const char* p = pos;
	while (p > buf_start && *(p - 1) != '\n') p--;
	return p;
}

const char* find_line_end(const char* s, const char* buf_end)
{
	for (const char* p = s; p < buf_end; p++)
		if (*p == '\n' || *p == '\0') return p;
	return buf_end;
}

// ============================================================================
// ImGui key repeat helper
// ============================================================================

bool key_pressed(int key)
{
	ImGuiIO& io = ImGui::GetIO();
	ImGuiKeyData& kd = io.KeysData[key - ImGuiKey_NamedKey_BEGIN];
	if (!kd.Down) return false;
	f32 t = kd.DownDuration;
	if (t == 0.0f) return true;
	f32 delay = io.KeyRepeatDelay;
	f32 rate = io.KeyRepeatRate;
	if (t < delay) return false;
	f32 prev = t - io.DeltaTime;
	if (prev < delay) return true;
	return (i32)((t - delay) / rate) > (i32)((prev - delay) / rate);
}
